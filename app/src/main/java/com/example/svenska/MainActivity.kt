    fun updateCount() {
        val cnt : TextView = findViewById(R.id.Count)
        cnt.setText(CUR_WORDS.size.toString())
    }
    fun hyphenate(word : String) : String {
        var len = word.length
        var MAX_LEN = 12
        if (len > MAX_LEN) {
            var tmp = word.subSequence(0, MAX_LEN-2).toString() + "-\n".toString() + word.subSequence(MAX_LEN-2,len)
            return tmp
        } else {
            return word
        }
    }
    fun deleteWord(word: String) {
        CUR_WORDS.remove(word)
        if (CLASS == "adjektiv") {
            if (ADJ.contains(word)) {
                ADJ.remove(word)
                CUR_WORDS = ADJ
            }
        } else if (CLASS == "verb") {
            if (VERB.contains(word)) {
                VERB.remove(word)
                CUR_WORDS = VERB
            }
        } else if (CLASS == "adverb") {
            if (ADVERB.contains(word)) {
                ADVERB.remove(word)
                CUR_WORDS = ADVERB
            }
        } else if (CLASS == "sub_en") {
            if (EN.contains(word)) {
                EN.remove(word)
                CUR_WORDS = EN
            }
        } else if (CLASS == "sub_ett") {
            if (ETT.contains(word)) {
                ETT.remove(word)
                CUR_WORDS = ETT
            }
        } else if (CLASS == "test") {

            if (TEST.contains(word)) {
                TEST.remove(word)
                CUR_WORDS = TEST
            }
        }
        updateCount()
    }
    // Update in-memory word listing if word is new
    // Then write to non-volatile storage for future usage
    fun addWord(view: View) {
        val inp : TextView = findViewById(R.id.newWord)
        var word = inp.getText().toString()
        if (word.length == 0) return
        // Non alphabetic charecter means delete the following word
        if (word[0] == '.') {
            val newWord = word.subSequence(1,word.length).toString()
            deleteWord(newWord)
            return
        }

        // Word listing contains only unique words
        for (i in 0 until CUR_WORDS.size) {
            if (word == CUR_WORDS[i].toString()) {
                return
            }
        }

        // New words go to non-volatile memory for future usage
        updateMemListing(word)

        // Write updated listing to non-volatile memory for future usage

        var context = getApplicationContext()
        var path = context.getFilesDir()
        val file = File(path, CLASS)

        var out = TEST[0]
        for (i in 1 until CUR_WORDS.size) {
            out += "\n" + TEST[i]
        }
        if (!file.exists()) file.createNewFile()

        file.writeText(out)
        inp.setText("")
        val cnt : TextView = findViewById(R.id.Count)
        updateCount()
    }
    fun updateMemListing(word: String) {
        var added = false
        if (CLASS == "test") {
            if (TEST.size == 0) {
                TEST.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < TEST[i].toString()) {
                        TEST.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) TEST.add(word)
            CUR_WORDS = TEST
        } else if (CLASS == "adjektiv") {
            if (TEST.size == 0) {
                TEST.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < ADJ[i].toString()) {
                        ADJ.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) ADJ.add(word)
            CUR_WORDS = ADJ
        } else if (CLASS == "verb") {
            if (VERB.size == 0) {
                VERB.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < VERB[i].toString()) {
                        VERB.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) VERB.add(word)
            CUR_WORDS = VERB
        } else if (CLASS == "adverb") {
            if (ADVERB.size == 0) {
                ADVERB.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < ADVERB[i].toString()) {
                        ADVERB.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) ADVERB.add(word)
            CUR_WORDS = ADVERB
        } else if (CLASS == "sub_en") {
            if (EN.size == 0) {
                EN.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < EN[i].toString()) {
                        EN.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) EN.add(word)
            CUR_WORDS = EN
        } else if (CLASS == "sub_ett") {
            if (ETT.size == 0) {
                ETT.add(word)
            } else {
                for (i in 0 until CUR_WORDS.size) {
                    if (word < ETT[i].toString()) {
                        ETT.add(i,word)
                        added = true
                        break
                    }
                }
            }
            if (!added) ETT.add(word)
            CUR_WORDS = ETT
        }
    }
    fun back(view: View) {
        var inc : TextView = findViewById(R.id.ind)

        INC -= (2*N_WORDS)
        if (INC < 0) {
            INC = 0
        }
        inc.setText(INC.toString())
        updateWord()
    }
    fun updateWords(view: View) {
        updateWord()
    }

    fun seek(view: View) {
        val iSeek : TextView = findViewById(R.id.newWord)
        val match = iSeek.getText()
        if (match.length == 0) return
        val mCh = match[0].toLowerCase()
        // Search first from current position
        val isFound = false
        for (i in INC until CUR_WORDS.size) {
            val tmp = getWord(i)
            val wCh = tmp[0]
            if (mCh == wCh) {
                INC = i
                val isFound = true
                updateWord()
                break
            }
        }
        // Search remaining word before current point
        if (!isFound ) {
            for (i in 0 until INC) {
                val tmp = getWord(i)
                val wCh = tmp[0]
                if (mCh == wCh) {
                    INC = i
                    updateWord()
                    break
                }
            }
        }

    }
