class MainActivity : Activity() {
    var INC = 0;
    var CLASS : String = ""
    var LOOP_PERIOD = 1000L
    var N_WORDS=8;
    val adj = Adjective()
    val verb = Verb()
    val adverb = Adverb()
    val sub_en = Sub_en()
    val sub_ett = Sub_ett()
    val test = Test()
    var HOLD = 0
    var TEST : MutableList<String> = mutableListOf()
    var VERB : MutableList<String> = mutableListOf()
    var ADJ : MutableList<String> = mutableListOf()
    var ADVERB : MutableList<String> = mutableListOf()
    var EN : MutableList<String> = mutableListOf()
    var ETT : MutableList<String> = mutableListOf()
    var CUR_WORDS : MutableList<String> = mutableListOf()

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main)
        val spinner: Spinner = findViewById(R.id.classSelector)
        val dataAdapter: ArrayAdapter<String> =
            ArrayAdapter<String>(
                this,
                R.layout.spinner_test,
                getResources().getStringArray(R.array.wordClasses)
            )
        // Drop down layout style - list view with radio button
        dataAdapter.setDropDownViewResource(R.layout.spinner_test)
        // attaching data adapter to spinner
        spinner.adapter = dataAdapter
        spinner.selected {
            INC = 0
            CLASS=spinner.getSelectedItem().toString();
            showWord()
        }

    }
    fun Spinner.selected(action: (position:Int) -> Unit) {
        this.onItemSelectedListener = object : AdapterView.OnItemSelectedListener {
            override fun onNothingSelected(parent: AdapterView<*>?) {}
            override fun onItemSelected(parent: AdapterView<*>?, view: View?, position: Int, id: Long) {
                action(position)
            }
        }
    }

    fun showWord() {
        if (CLASS == "verb") {
            showVerb()
        } else if (CLASS == "adjektiv") {
            showAdj()
        } else if (CLASS == "adverb") {
            showAdverb()
        } else if (CLASS == "en") {
            showSubEn()
        } else if (CLASS == "ett") {
            showSubEtt()
        } else if (CLASS == "test") {
            showTest()
        }
    }
    fun countEl(s: String): Int {
        return s.filter { it == '\n' }.count()
    }
    // One-time initialisation of word lists from static information
    fun updateFile() {
        var context = getApplicationContext()
        var path = context.getFilesDir()
        val file = File(path, CLASS)
        println("Updating file " + file.toString() +  " from dynamic in-memory word listing...")

        if (CLASS == "test") {
            var txt = ""
            for (i in 0 until CUR_WORDS.size) {
                var del = "\n"
                if (i == 0) del = ""
                txt += del + TEST[i]
            }
            file.writeText(txt)
        }
    }

    fun createFile() {
        var cnt : TextView = findViewById(R.id.Count)
        cnt.setText("0")
        var context = getApplicationContext()
        var path = context.getFilesDir()
        val file = File(path, CLASS)
        println("Creating file " + file.toString() +  " from static info...")
        file.createNewFile()
        if (CLASS == "verb") {
            for (word in verb.verb) {
                file.appendText(word)
            }
            for (i in 0 until verb.verb.size) {
                VERB.add(verb.verb[i])
            }
        } else if (CLASS == "adjektiv") {
            for (word in adj.adj) {
                file.appendText(word)
            }
            for (i in 0 until adj.adj.size) {
                ADJ.add(adj.adj[i])
            }
        } else if (CLASS == "adverb") {
            for (word in adverb.adverb) {
                file.appendText(word)
            }
            for (i in 0 until adverb.adverb.size) {
                ADVERB.add(adverb.adverb[i])
            }
        } else if (CLASS == "sub_en") {
            for (word in sub_en.sub_en) {
                file.appendText(word)
            }
            for (i in 0 until sub_en.sub_en.size) {
                EN.add(sub_en.sub_en[i])
            }
        } else if (CLASS == "sub_ett") {
            for (word in sub_ett.sub_ett) {
                file.appendText(word)
            }
            for (i in 0 until sub_ett.sub_ett.size) {
                ETT.add(sub_ett.sub_ett[i])
            }
        } else if (CLASS == "test") {
            for (word in test.test) {
                file.appendText(word)
            }
            for (i in 0 until test.test.size) {
                TEST.add(test.test[i])
            }
        }
        updateCount()
    }

    fun checkFile() {
        var context = getApplicationContext()
        var path = context.getFilesDir()
        val file = File(path, CLASS)
        if (file.exists()) {
            var count = countEl(file.readText())
            if (count < 10) {
                createFile()
            }
        } else {
            createFile()
        }
    }
    fun getWord(i: Int) : String {
        if (i+1 > CUR_WORDS.size) return ""
        return hyphenate(CUR_WORDS[i])
    }

    fun updateWord() {
        val display_text : TextView = findViewById(R.id.iWordList)
        var txt=""
        var tmp  = getWord(INC)
        var passover = 0
        txt = tmp
        if (tmp.contains("-")) {
            passover++
        }
        tmp = ""
        for (ind in 1..7) {
            if (INC + ind > CUR_WORDS.size -1 ) {
                break
            }
            if (ind < N_WORDS - passover) {
                tmp = getWord(INC+ind)
                if (tmp.contains("-")) {
                    passover++
                }
                txt += "\n" + tmp
            }
        }
        val inc : TextView = findViewById(R.id.ind)
        inc.setText((INC+1).toString())
        if (INC + N_WORDS + passover < CUR_WORDS.size) {
            INC += N_WORDS - passover
        }
        display_text.setText(txt)
    }
    fun showVerb() {
        CLASS = "verb"
        if (VERB.size == 0) {
            checkFile()
        }
        CUR_WORDS.clear()
        CUR_WORDS = VERB
        updateCount()
        updateWord()
    }
    fun showAdj() {
        CLASS = "adjektiv"
        if (ADJ.size == 0) {
